//using classList
//obj.classList

//classList.add() to add new classes

//classList.remove() to remove classes 

//classList.containes() to check if class exists

//classList.toggle() to toggle between add & remove 

let heading = document.querySelector('h1');
heading.classList;

//We can add classes 
heading.classList.add("green");

heading.classList.add("underline");

//can remove the class

// heading.classList.remove("green");


//Contain Method 

heading.classList.contains("green");

//Toggle Method:- if class exist then toggle will remove if class not exist then toggle will add the class

heading.classList.toggle("green");